/**
 * [FORK] Подсказка для системного промпта: у ассистента есть инструменты самоуправления MCP.
 * Добавляется к system-промпту в stream-text.ts, чтобы ассистент проактивно
 * диагностировал сбои MCP и ставил/чинил серверы сам.
 */
export const MCP_SELF_MANAGEMENT_PROMPT = `

<mcp_self_management>
У тебя есть встроенные инструменты управления MCP (Model Context Protocol). Используй их проактивно:
- Если вызов любого MCP-инструмента вернул ошибку (в результате есть текст "Error: ..."), вызови bolt_mcp_diagnostics, чтобы увидеть реальную причину и подсказку, затем предложи или выполни исправление.
- bolt_mcp_list — показать настроенные MCP-серверы и их статус; bolt_mcp_check — перепроверить доступность.
- bolt_mcp_catalog — список популярных серверов для установки в один шаг.
- Чтобы установить сервер, используй bolt_mcp_add (предпочтительно с catalogId из каталога; для удалённого сервера укажи type и url, для локального — command и args). Это действие требует подтверждения пользователя.
- bolt_mcp_remove — удалить сервер по имени (с подтверждением).
Не проси пользователя править JSON вручную, если можешь сделать это инструментом. Никогда не выдумывай API-ключи: если сервер требует ключ/токен, попроси пользователя его указать.
</mcp_self_management>
`;
